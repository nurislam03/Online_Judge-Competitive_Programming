
/* Write a query to display all customers with a grade above 100. */
Select *
From Customers
Where Grade > 100

/* Write a query statement to display all customers in your home town who have a grade value above 100. */
Select *
From Customers
Where (
    Location = 'Bogra'
    Grade > 100
)

