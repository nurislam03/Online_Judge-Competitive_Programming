vector < string > grid;
int vis[SIZE+1][SIZE+1];
int row, col;


bool IsValid(int a, int b){
    return((a < row && a >= 0) && (b < col && b >= 0)) ? true:false;
}

int BFS(int x, int y)
{
    queue <pii> q;
    q.push(pii(x,y));
    //pair <int, int> Pr;
    vis[x][y] = 1;
    int cnt = 1;

    while(!q.empty())
    {
        pii top = q.front();
        q.pop();

        for(int i = 0; i < 8; i++)
        {
            int u = top.first  + fx8[i];
            int v = top.second + fy8[i];

            if(!IsValid(u, v)) continue;
            if(vis[u][v] != -1 || grid[u][v] == '.') continue;

            cnt++;
            vis[u][v] = 1;
            q.push(pii(u, v));
        }
    }

    return cnt;
}



int main()
{
    //read("input.txt");
    //write("output.txt");
    while(scanf("%d %d",&row, &col) == 2)
    {
        if(!row && !col) break;

        grid.clear();
        string line;
        for(int i = 0; i < row; ++i)
        {
            cin >> line;
            grid.push_back(line);
        }

        memset(vis, -1, sizeof vis);
        int startCnt = 0;

        for(int i = 0; i < row; i++)
        {
            for(int j = 0; j < col; j++)
            {
                if(vis[i][j] != -1 || grid[i][j] == '.') continue;
                int cnt = BFS(i, j);

                if(cnt == 1) startCnt++;
            }
        }
        printf("%d\n",startCnt);
    }

    return 0;
}

